import UIKit

extension UIColor {
    @objc static var projectBlack: UIColor {
        return UIColor(red: 29/255, green: 30/255, blue: 32/255, alpha: 1.0)
    }
    
    @objc static var primaryColor: UIColor {
        return UIColor(red: 33/255, green: 194/255, blue: 94/255, alpha: 1.0)
    }
}
